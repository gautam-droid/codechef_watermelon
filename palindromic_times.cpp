    int rem, rev;
    cin >> t;
    string hr = t.substr(0, 2);
    string mins = t.substr(3, 5);
    temp=hr;
    int minss = stoi(mins);
    reverse(temp.begin(), temp.end());
    rev = stoi(temp);
