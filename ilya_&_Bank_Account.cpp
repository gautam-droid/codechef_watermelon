#include <iostream>
#include <string>
using namespace std;
int main()
{
    int n, maxi;
    cin >> n;
    auto num_str=to_string(n);
    auto len=num_str.length();
    if (n > 0)
    {
        if (n == 1)
        {
            cout << "0" << n << endl;
            return 0;
        }
        else
            maxi = n;
    }
    else
    {
        if (len <= 2)
        {
            maxi = max(n / 10, n % 10);
            // cout<<n/10<<" "<<n%10<<endl;
        }
        else
        {
            num_str.erase(len-2,1);
            // cout<<num_str<<endl;
            int temp=stoi(num_str);
            maxi = max(n / 10, temp);
            // cout<<temp<<endl;
            // cout<<n/10;
        }
    }
    cout << maxi;
    return 0;
}