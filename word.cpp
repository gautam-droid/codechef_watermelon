#include <iostream>
#include <cctype>
#include <string>
using namespace std;
int main()
{
    string str;
    cin >> str;
    int c_up = 0, c_low = 0;
    for (char c : str)
    {
        if (isupper(c))
        {
            c_up++;
        }
        else if (islower(c))
        {
            c_low++;
        }
    }
    if (c_up > c_low)
    {
        for(int i=0;i<str.length();i++)
        {
         str[i]=toupper(str[i]);
        }
    }
    else
    {
       for(int i=0;i<str.length();i++)
        {
         str[i]=tolower(str[i]);
        }
    }
    cout<<str;
    // cout<<c_low<<c_up;
    return 0;
}